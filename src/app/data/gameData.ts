/**
 * Pick random questions for a game session.
 */
export function pickQuestions(
  count: number,
  category?: GameCategory,
  minDifficulty?: Difficulty,
  maxDifficulty?: Difficulty,
  excludeIds: string[] = [],
): GameQuestion[] {
  let pool = QUESTION_BANK.filter(q => !excludeIds.includes(q.id));
  if (category) pool = pool.filter(q => q.category === category);
  if (minDifficulty) pool = pool.filter(q => q.difficulty >= minDifficulty);
  if (maxDifficulty) pool = pool.filter(q => q.difficulty <= maxDifficulty);

  // Fisher-Yates shuffle
  const shuffled = [...pool];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  return shuffled.slice(0, Math.min(count, shuffled.length));
}

/**
 * Get today's date as string for daily play tracking.
 */
export function getTodayKey(): string {
  return new Date().toISOString().slice(0, 10);
}
